package solidbase.http;

public class JspServlet implements Servlet
{
	public void call( RequestContext request )
	{
		String url = request.request.url;
		if( url.startsWith( "/" ) )
			url = url.substring( 1 );
		url = url.replaceAll( "[\\\\/]", "." );
		url = url.replaceAll( "[\\.-]", "_" );
		request.applicationContext.callJsp( url, request );
	}
}
